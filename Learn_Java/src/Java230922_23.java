public class Java230922_23 {
    public static void main(String[] args) throws Exception {
        int n = 3;
        switch (n) {
        case 1: 
            System.out.println("n is one.");
            break;
        case 2:
            System.out.println("n is two.");
            break;
        case 3:
            System.out.println("n is three.");
            break;
        default:
            System.out.println("unknown");
            break;
        }


        }
        
}
